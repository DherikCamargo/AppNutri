  function autocompletar(campoNumero) {
    const inputCampo = document.getElementById(`alimento${campoNumero}`);
    const textoDigitado = inputCampo.value.toLowerCase();
    const divSugestoes = document.getElementById(`divSugestoes${campoNumero}`);
  
    // Limpar sugestões anteriores
    divSugestoes.innerHTML = "";
  
    // Filtrar as sugestões com base no texto digitado
    const sugestoes = alimentos.filter((alimento) =>
      alimento.nome.toLowerCase().startsWith(textoDigitado)
    );
  
    // Adicionar as sugestões à div de sugestões
    sugestoes.forEach((sugestao) => {
      const sugestaoElemento = document.createElement("div");
      sugestaoElemento.textContent = sugestao.nome;
      sugestaoElemento.addEventListener("click", function () {
        inputCampo.value = sugestao.nome;
        divSugestoes.style.display = "none";
      });
      divSugestoes.appendChild(sugestaoElemento);
    });
  
    // Exibir a div de sugestões
    divSugestoes.style.display = sugestoes.length > 0 ? "block" : "none";
  }
  

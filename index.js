  function formatarTexto(texto) {
    return texto.toLowerCase().replace(/[.,#!$%^&*;:{}=\-_`~()]/g, "").replace(/\b\w/g, (match) => match.toUpperCase());
  }
  
  function buscarCalorias(alimento) {
    const alimentoEncontrado = alimentos.find((f) => formatarTexto(f.nome) === formatarTexto(alimento));
    return alimentoEncontrado;
  }
  
  function calcularCalorias() {
    const inputs = document.querySelectorAll("#caloriasForm input[type='text']");
    const resultado = document.getElementById("resultado");
    resultado.innerText = "";
  
    let totalCalorias = 0;
  
    inputs.forEach((input) => {
      const alimento = input.value;
      const alimentoEncontrado = buscarCalorias(alimento);
  
      if (alimentoEncontrado) {
        const calorias = alimentoEncontrado.calorias;
        totalCalorias += calorias;
      }
    });
  
    resultado.innerText = `Total de calorias: ${totalCalorias} calorias`;
  }
  

